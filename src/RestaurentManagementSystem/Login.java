    private void btn_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regActionPerformed
        // TODO add your handling code here:
        
        
        String rname,rusername,rpass,rcontactno,raddress;
        
        rname = ET_regName.getText();
        rusername = ET_regUsername.getText();
        rpass = ET_regPass.getText();
        rcontactno = ET_regContact.getText();
        raddress = ET_regAddss.getText();
        try{

            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=MISHU;integratedSecurity=true");

            Statement statement = connection.createStatement();

            Boolean resultSet = statement.execute("INSERT INTO Customers (name,contact_no,addresss,username) VALUES ('"+rname+"','"+rcontactno+"','"+raddress+"','"+rusername+"');"
                    + "INSERT INTO users (username,pass) VALUES ('"+rusername+"','"+rpass+"');");
            //ResultSet rs = statement.executeQuery("SELECT * FROM Customers WHERE Cus_ID = (SELECT IDENT_CURRENT('Customers'))");

            if(!resultSet){
                L_sataus.setText("SUCCESSFULLY REGISTERED!");           
            }

        } catch (Exception e) {
            e.printStackTrace();
            L_sataus.setText("REGISTRATION FAILED");
            L_sataus.setForeground(Color.red);
        }
        
    }//GEN-LAST:event_btn_regActionPerformed

