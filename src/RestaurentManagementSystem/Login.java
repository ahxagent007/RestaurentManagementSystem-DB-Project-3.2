    private void BTN_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_loginActionPerformed
        // TODO add your handling code here:

        String username= TF_userEmail.getText().trim();
        String pass = TF_password.getText().trim();
        // where id="+s1+" and password="+s2+"

        try{

            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=MISHU;integratedSecurity=true");

            Statement statement = connection.createStatement();

            ResultSet resultSet = statement.executeQuery("SELECT email, pass from Customer where email='"+username+"' and pass='"+pass+"' ");
            ResultSet resultSetsss = statement.executeQuery("SELECT * from Customers where username='"+username+"'");

            if(resultSetsss.next()){
                L_sataus.setText("LOGIN SUCCESS");
                String CusID = resultSetsss.getString("Cus_ID");
                new RestaurentList(CusID).setVisible(true);
                this.setVisible(false);
                
            }else{
                L_sataus.setText("LOGIN FAILED");
                L_sataus.setForeground(Color.red);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_BTN_loginActionPerformed

